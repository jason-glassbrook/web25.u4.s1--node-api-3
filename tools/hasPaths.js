/***********************************************************
  hasPaths
------------------------------------------------------------
  like _.has, but checks a list of paths
***********************************************************/

const _ = require ('lodash/fp')

/***************************************
  definition
***************************************/

const hasPaths = (paths) => /* (collection) => */ (
  _.overEvery (
    _.map (_.has) (paths)
  ) /* (collection) */
)

/**************************************/

module.exports = hasPaths
